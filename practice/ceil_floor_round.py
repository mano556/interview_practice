# ceil means COVER 
import math

a=7.5
print(math.ceil(a))
#  below or above 7.5 whatever it consider to 8 covers it 


# Floor means BRING DOWN
import math 
a=7.5
print(math.floor(a))
#  below or above 7.5 whatever it consider to 7 bring down it 

# Round means round off 
x=7.6
print(round(x))
# this is not math class method,this is general method 
# this one is normal like below 7.5 means 7,above 7.5 means 8 (including 7.5)